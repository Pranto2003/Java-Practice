import java.lang.*;
public class Wrapper{
	public static void main(String[] args){
		Integer y=100;
		Byte a=10;
		System.out.println("For Integer :");
		System.out.println("SIZE = "+ y.SIZE);
		System.out.println("Minimum Value = "+ y.MIN_VALUE);
		System.out.println("Maximum Value = "+ y.MAX_VALUE +"\n\n\n");
		//System.out.println(Integer.parseInt("123")+y);
		System.out.println("For Byte :");
		System.out.println("SIZE = "+ a.SIZE);
		System.out.println("Minimum Value :"+ a.MIN_VALUE);
		System.out.println("Maximum Value :"+ a.MAX_VALUE+"\n\n\n");
		
	}
}
